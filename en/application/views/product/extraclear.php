      <p><b>The Rhino Ecowash Extra Clear is an automatically activated drive through spray wash system incorporating the Ecoclear, the worldâ€™s most effective, compact water recycling system for use in wheel washing applications. Together, they prevent the spread of site dirt and debris on to public highways. Like many systems in the Rhino Ecowash range, the Extra Clear is powered by inverter-driven pumps to provide up to 50% operational cost savings, unrivalled reliability and total flexibility of wash pressure.</b></p>
      <p>The Ecowash Extra Clear comprises a robust 4m or 6m wash platform, optional 3m ramps and the Ecoclear waste water treatment system which has an effective settlement capacity exceeding 200m<sup>2</sup>. All equipment is made from heavy duty steel. It is supplied with quality pumps and controls housed in the lockable kiosk located at the front of a header tank.</p>
      <p>The newly-improved wash platform benefits from 8 strategically positioned water delivery pipes that deliver narrow fans of spray up to 4.2 bar to a targeted location on each tyre. The staggered positioning of the nozzles ensures the whole tyre is cleaned without the occurrence of striping. The spray pattern is configured to minimise overspray and high screens come as standard to ensure wash water is deflected back into the wash platform. The wash operates with on-off sensors ensuring water is only sprayed when a vehicle is present in the wash platform.</p>
      <p>A unique 3-chamber side sump, removable grids and improved separate wash down programme make it the easiest system available to clean and maintain. Guide rails are supplied in sections for easy replacement in the unlikely event of damage.</p>
      <p>Requiring only 12m<sup>2</sup>, the Ecoclear is a simple-to-use lamella clarifier that has an effective surface area equivalent to 16 standard settlement tanks in a row or 2 standard lagoons. This is because its internal area is filled with lamella blocks. Compared to such tanks and lagoons, more suspended particles settle out of the water, quicker because they only have a few centimetres to fall before contacting a surface, as opposed to a few metres. Once the particles contact a surface, they settle out of the water and collect in the base and form sludge. A gate valve is fitted to the bottom of the hopper for easy sludge removal. High quality recycled water flows into a header tank which is supplied to ensure there is always sufficient clean water for use on demand. A mechanical ball valve enables automatic top-up from a mains water supply, when required.</p>
