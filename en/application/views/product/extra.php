      <p><b>The Rhino Ecowash Extra is an automatically activated drive through spray wash system designed to prevent the spread of site dirt and debris on to public highways. Like many systems in the Rhino Ecowash range, it is powered by inverter-driven pumps to provide up to 50% operational cost savings, unrivalled reliability and total flexibility of wash pressure. This closed loop system also effectively recycles the water it uses.</b></p>
      <p>The Rhino Ecowash Extra comprises a robust 4m or 6m wash platform, optional 3m ramps and water recycling tanks totalling 36,000 litres capacity. All items are made from heavy duty steel. The system is supplied with quality pumps and controls housed in a lockable kiosk.</p>
      <p>The newly-improved wash platform benefits from 8 strategically positioned water delivery pipes that deliver narrow fans of spray up to 4.2 bar to a targeted location on each tyre. The staggered positioning of the nozzles ensures the whole tyre is cleaned without the occurrence of striping. The standard spray pattern is configured to minimise overspray and high side screens come as standard to ensure wash water is deflected back into the wash platform. The wash operates with on-off sensors ensuring water is only sprayed when a vehicle is present in the wash platform.</p>
      <p>A unique 3-chamber side sump, removable grids and improved separate wash down programme make it the easiest system available to clean and maintain. Guide rails are supplied in sections for easy replacement in the unlikely event of damage.</p>
      <p>The water recycling tank is divided into three sections by baffle and weir plates to aid particulate settlement and water quality. Each chamber is fitted with valves for easy sludge removal. A high capacity header tank is supplied to ensure there is always sufficient clean water for use on demand. A mechanical ball valve enables automatic top-up from a mains water supply, when required.</p>
      <p>This system is supplied with a hand lance as standard for additional, mobile washing.</p>
