    <div class="productleft">
      <p><b>The Rhino Ecowash Express is the smallest of the high pressure spray wash systems, designed for the tightest of spaces. Like all systems in the Rhino Ecowash range, it provides, unrivalled reliability and performance. Operating on a closed loop system, it also effectively recycles the water it uses.</b></p>
      <p>The Rhino Ecowash Express comprises a robust 2m wash platform, optional 3m ramps and a water recycling tank totalling 4,000 litres capacity, all made from heavy duty steel. It is supplied with quality submersible pumps and controls housed in an IP65 rated cabinet.</p>
      <p>The new wash platform benefits from 6 strategically positioned water delivery pipes that spray a targeted curtain of water at 1.5 bar, reaching the whole surface of each tyre for a quick and effective clean. The spray pattern is configured to minimise overspray. As an additional space, water and cost saving feature, one side screen is integrated into the side of the adjacent water recycling tank. Together, they ensure wash water is deflected back into the platform for recycling.</p>
      <p>The submersible recovery pump sits in an external sump adjacent to the wash platform. Along with the removable grids and improved separate wash down programme, these design elements make it the easiest system available to clean and maintain.</p>
      <p>The water recycling tank is divided in two by a filter plate to ensure the submersible wash pump only pumps clarified water. Sludge removal is easily performed either by opening the valve at the base or via mechanical bucket from above. Fitted with a mechanical ball valve, it will top up automatically from a mains water supply, when required. The Rhino Ecowash Express is delivered on a crane offload vehicle and can be installed within a day.</p>
