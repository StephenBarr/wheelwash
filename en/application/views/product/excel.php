      <p><b>The Rhino Ecowash Excel is a reliable and compact high pressure spray wash system that operates automatically. Like all systems in the Rhino Ecowash range, it provides unrivalled reliability and performance. The closed loop system also effectively recycles the water it uses.</b></p>
      <p>The Rhino Ecowash Excel comprises a robust 4m wash platform, optional 3m ramps and a water recycling tank totalling 15,000 litres capacity, all made from heavy duty steel. It is supplied with quality submersible pumps and the controls are housed in a IP65 rated cabinet.</p>
      <p>The newly-improved wash platform benefits from 8 strategically positioned water delivery pipes that spray narrow fans of water up to 3 bar to a targeted location on each tyre. The staggered positioning of the nozzles ensures the whole tyre is cleaned without the occurrence of striping. Manual shut off valves are fitted to provide a personalised spray configuration, if desired. The standard spray pattern is configured to minimise overspray and high-side screens come as standard to ensure any deflected water is directed back into the wash platform. The wash operates on a timer that can be adjusted to suit the changing needs of site.</p>
      <p>A unique 3-chamber side sump, removable grids and improved separate wash down programme make it the easiest system available to clean and maintain. Guide rails are supplied in sections for easy replacement in the unlikely event of damage.</p>
      <p>The water recycling tank is divided into three sections by baffle and weir plates to aid particulate settlement and water quality. Sludge removal is easily performed either by opening the valve at the base of each chamber or via mechanical bucket from above. A clean head of water is located in the final chamber which ensures there is always sufficient clean water for use on demand. A mechanical ball valve enables automatic top-up from a mains water supply, when required.</p>
