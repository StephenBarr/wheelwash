      <p><b>El Rhino Ecowash Express es el más pequeño de los sistemas de lavado por aspersión de agua a alta presión. Diseñado para los espacios más reducidos, como todos los sistemas de la gama Rhino Ecowash, proporciona un rendimiento sin igual y una elevada fiabilidad. Su modelo de operación en circuito cerrado permite la máxima reutilización del agua.</b></p>
	  <p>El Rhino Ecowash Express se compone de una robusta plataforma de lavado de 2m, cuatro rampas opcionales de 3m y un depósito para el reciclaje de agua con una capacidad de 4000 litros. Todo fabricado de acero de alta resistencia. El equipo se suministra con bombas sumergibles de alta calidad y sistema de control alojado en un armario de clasificación IP65.</p>
	  <p>La nueva plataforma de lavado viene partida a fin de facilitar su transporte, posee 6 barras de aspersión colocadas estratégicamente para obtener una cortina de agua a 1,5 bar capaz de llegar a toda la superficie de cada rueda obteniendo una limpieza rápida y eficaz. La posición de las boquillas está diseñada para minimizar el exceso de pulverización. Otra ventaja adicional para el ahorro de espacio, agua y costes, es que una de las pantallas laterales está integrada en el lateral del tanque de reciclado de agua adyacente. Juntos, se aseguran que el agua de lavado es devuelta nuevamente a la plataforma para su reutilización.</p>
	  <p>Una bomba de recuperación de lodos sumergible se asienta en un sumidero situado al lado de la plataforma de lavado entre la pantalla lateral y las barras de protección. Las rejillas extraíbles, así como un programa de lavado automático mejorado, hacen que sea el sistema más fácil de limpiar y mantener.</p>
	  <p>El depósito de sedimentación se encuentra dividido en dos partes por una placa perforada, que hace la función de filtro para asegurar que la bomba de lavado sumergible sólo bombee agua. La eliminación de los lodos sedimentados se realiza con facilidad, bien mediante la apertura de la válvula situada en la base del tanque o a través de una cuba mecánica desde la parte superior. Equipada con una válvula de flotador, el depósito de agua se rellena automáticamente desde un suministro principal de agua, cuando sea necesario.</p>
	  <p>El Ecowash Express puede ser montado superficie o enterrado y por lo general puede ser instalado en un solo día.</p>
