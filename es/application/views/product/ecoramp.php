    <div class="productleft">
      <p><b>El Rhino Ecoramp es un sistema sencillo, diseñado para evitar la propagación de la suciedad y los escombros en las vías públicas. Es nuestro sistema de limpieza de ruedas más ecológico ya que no requiere energía, ni agua y además no requiere prácticamente mantenimiento. La limpieza se basa en el efecto de vibración creada por los neumáticos de los vehículos al circular sobre los angulares.</b></p>
      <p>El sistema está compuesto por tramos móviles y robustos en secciones horizontales
de 4m y rampas de entrada y salida inclinadas de perfil bajo de 3m, todo ello fabricado en acero de alta resistencia. Cada una de las secciones está dotado de barreras con el fin de proporcionar una guía visual a los conductores. El montaje se realiza mediante enclavamientos a ambos extremos de las secciones, asegurando un rápido y seguro acoplamiento entre las piezas. Acoplamientos transversales, colocados a intervalos regulares, a fin de proporcionar una mayor rigidez.</p>
      <p>El Rhino Ecoramp dispone de dos versiones, una de 18m apta para vehículos de hasta
8 ruedas y una versión de 22 metros para vehículos articulados. Debido a su formato modular, el Ecoramp podría suministrarse para cualquier longitud a fin de satisfacer sus necesidades de espacio, uso y presupuesto.</p>
      <p>Los vehículos subirán a la plataforma a través de las rampas de entrada y conducirán
a través de las secciones de la plataforma para conseguir entre 5.5 y 6.5 revoluciones
de rueda. Este movimiento abre las bandas de rodadura permitiendo que
la suciedad y los escombros caigan al suelo. Los diferentes componentes del sistema pueden ser levantados fácilmente mediante la maquinaria adecuada para eliminar la suciedad acumulada.</p>
      <p>El sistema es ideal para ubicaciones remotas o sitios donde no existan servicios disponibles. Se entrega en un camión Hiab, es fácil de instalar y trasladar.</p>
