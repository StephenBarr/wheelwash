      <p><b>El Rhino Ecowash ExtraClear es un sistema de lavado automático por aspersión de agua a alta presión que incorpora el novedoso filtro de lamelas EcoClear, el sistema de filtración más compacto y eficaz a nivel mundial para utilizar en aplicaciones de lava ruedas. Juntos previenen la extensión de lodos y residuos a las carreteras. Al igual que muchos de los sistemas de la gama Rhino Ecowash, el ExtraClear funciona con bombas alimentadas a través de variadores de velocidad, lo que proporciona ahorros de hasta un 50% en los costes operacionales, un rendimiento sin igual, una elevada fiabilidad y una alta flexibilidad de presión de agua.</b></p>
	  <p>El Rhino Ecowash Extra se compone de una robusta plataforma de lavado de 4m o 6m, cuatro rampas opcionales de 3m o 5m y el sistema de Tratamiento de Agua Residual EcoClear que tiene una capacidad de sedimentación que excede 200m<sup>2</sup>. Todo fabricado en acero de alta resistencia. Las bombas son de alta calidad y el sistema de control está alojado en una sala de bombas dotada de cerradura ubicada al lado del depósito principal.</p>
	  <p>La nueva y mejorada plataforma de lavado posee 8 barras de aspersión colocadas estratégicamente para obtener una cortina de agua de hasta 4.2 bar capaz de llegar a toda la superficie de cada rueda. El posicionamiento escalonado de las boquillas asegura un lavado eficaz de la totalidad de la rueda. El posicionamiento de cada barra y boquilla está diseñada para minimizar el exceso de pulverización y las altas pantallas laterales vienen de serie para asegurar que el agua de lavado sea devuelta nuevamente a la plataforma de lavado. El ciclo de lavado es gobernado de forma automática mediante sensores de entrada y salida, esto asegura que el agua este solamente rociando cuando hay un vehículo presente dentro de la plataforma de lavado.</p>
	  <p>El sumidero lateral, rejillas desmontables y su sistema de lavado automático convierten al Ecowash Extra en un sistema fácil de limpiar y mantener. Barreras de protección desmontables en tramos para facilitar la sustitución de las mismas en el improbable caso de que se dañen.</p>
	  <p>Ocupando un área de sólamente 12m<sup>2</sup>, el Ecoclear es un clarificador de lamelas fácil de usar que tiene una superficie efectiva equivalente a 16 tanques de sedimentación en línea estándar o 2 lagunas estándar *. Esto es debido a que su área interna está llena de bloques de lamelas. En comparación con esos tanques y lagunas, mas partículas en suspensión saldrán fuera del agua, y más rápido, ya que sólo tienen unos pocos centímetros antes de contactar con alguna de las superficies, en lugar de los metros que tienen en los tanques convencionales. Una vez que las partículas se ponen en contacto con una superficie, salen del agua y se acumulan en la base formando lodos. Una válvula de compuerta se encuentra montada en la parte inferior de la tolva para la fácil eliminación de los lodos. Esta agua filtrada de alta calidad fluye al tanque de cabeza que se suministra para asegurar que siempre se disponga de suficiente agua limpia para responder a la demanda. Equipada con una válvula de flotador, el depósito de agua se rellena automáticamente desde un suministro principal de agua, cuando sea necesario.</p>
