	  <p>El Rhino Ecowash Excel se compone de una robusta plataforma de lavado de 4m, cuatro rampas opcionales de 3m y un depósito para el reciclaje de agua con una capacidad de 15.000 litros. Todo fabricado en acero de alta resistencia. Es el único sistema de lavaruedas compacto que viene con una potente bomba de impulsión, ubicada en una sala de bombas independiente adyacente al depósito. Esta bomba de alta eficacia ofrece una mejora en el rendimiento, y en la accesibilidad. El sistema de control está alojado en un armario de clasificación IP65.</p>
	  <p>La nueva y mejorada plataforma de lavado posee 8 barras de aspersión colocadas estratégicamente para obtener una cortina de agua a 3 bar capaz de llegar a toda la superficie de cada rueda. El posicionamiento escalonado de las boquillas asegura un lavado eficaz de la totalidad de la rueda. Se han instalado válvulas de cierre manuales para proporcionar una configuración de aspersión personalizada, si se desea. El posicionamiento de cada barra y boquilla está diseñada para minimizar el exceso de pulverización y las altas pantallas laterales vienen de serie para asegurar que el agua de lavado sea devuelta nuevamente a la plataforma de lavado. El sistema funciona con un temporizador que puede ser ajustado según las necesidades cambiantes del sitio.</p>
	  <p>El sumidero lateral, rejillas desmontables y su sistema de lavado automático convierten al Ecowash Excel en el sistema más fácil de limpiar y mantener. Barreras de protección desmontables en tramos para facilitar la sustitución de las mismas en caso de improbable daño de estas.</p>
	  <p>El depósito de sedimentación se encuentra dividido en tres partes por una placa perforada y deflectores para ayudar separar los residuos y mejorar la calidad del agua. La eliminación de los lodos sedimentados se realiza con facilidad, bien mediante la apertura de la válvula situada en la base de cada compartimiento o a través de una cuba mecánica desde la parte superior. Un punto de aporte de agua limpia ubicado en el último compartimiento asegura que siempre hay suficiente agua limpia para responder a la demanda. Equipada con una válvula de flotador, el depósito de agua se rellena automáticamente desde un suministro principal de agua, cuando sea necesario.</p>
