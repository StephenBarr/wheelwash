      <p><b>El Rhino Ecobath es un sistema de lavado sencillo para prevenir la extensión de lodos y residuos a las carreteras. Es un equipo de limpieza que respeta el medio ambiente y que sólo requiere 13.750 litros de agua para que funcione.</b></p>
	  <p>El sistema se compone de una sección de lavado central de 10m que contiene rejillas angulares internas fabricadas en acero, estas se encuentran parcialmente sumergidas en agua. Rampas adicionales de 3m de largo son suministradas cuando el sistema se instala en superficie. Se pueden suministrar si se desea tramos adicionales horizontales en secciones de 4m para proporcionar una limpieza adicional por medio de un efecto de vibración en seco. Estos pueden estar ubicados antes y después de la sección de lavado y lo hacen adecuado para su uso por los vehículos articulados.</p>
	  <p>El baño estándar de 10m proporciona un lavado a través del agua de 3 revoluciones de la rueda. Los ángulos de acero hacen que abran las bandas de rodadura cuando el vehículo pasa por encima, permitiendo que la suciedad caiga. El agua lava la superficie de las bandas de rodadura, lo que garantiza una limpieza eficaz. Las rejillas internas se quitan fácilmente para acceder a la base de la bañera para un mantenimiento fácil y periódico. Debido a su formato modular es posible suministrar cualquier longitud de Ecobath a fin de satisfacer las condiciones de espacio disponible, uso y presupuesto. Wheelwash también puede proporcionar versiones a medida bajo pedido.</p>
	  <p>El sistema Ecobath es perfecto para lugares remotos o para sitios donde no hay servicios disponibles. Se entrega en un camión Hiab, es fácil de instalar y trasladar. Los sistemas Ecobath están disponibles para la venta y alquiler.</p>
