      <p><b>El Sistema de Tratamiento de Agua Residual Ecoclear es el mejor clarificador de lamelas diseñado para utilizar en aplicaciones de lava ruedas. Ha sido desarrollado en colaboración con Scott Vickers Pte, que disponen de una dilatada experiencia a nivel mundial en la fabricación de la gama de sistemas Cleartec de limpieza de aguas residuales y superficiales para obras de construcción.</b></p>
	  <p>El EcoClear elimina de forma eficaz sólidos en suspensión de las aguas residuales generadas por los sistemas lava ruedas. A pesar de que solo dispone de una superficie de 12m<sup>2</sup>, el EcoClear tiene una capacidad de sedimentación que excede 200m<sup>2</sup>, que permite aclarar el agua residual a una calidad similar como si hubiera fluido a través de 16 depósitos de sedimentación en una fila o 2 lagunas estándares. Esto permite que el agua reciclada de alta calidad pueda ser reutilizada en el proceso de lavado de ruedas, maximizando así el rendimiento.</p>
	  <p>El agua residual entra por el sistema EcoClear a través de un depósito primario. Esto permite que el agua fluya más despacio y los sólidos más pesados se asienten con la ayuda de 2 placas de acero inclinadas instaladas internamente. A continuación, el agua fluye a la sección de clarificación donde están ubicados los bloques de lamelas. La calidad del agua tratada se atribuye a la gran superficie interna creada por los bloques de lamelas ubicados en este compartimiento. Proporcionan 192m<sup>2M</sup> superficie inclinada para que los sólidos en suspensión puedan aglomerar. Una vez aglomerados, los sólidos se acumulan en los bloques de lamelas y caen hacia la tolva formando lodos. La tolva está equipada con una válvula para facilitar el vaciado. Mientras tanto, el agua limpia sube a la parte superior del compartimiento y pasa por encima al depósito principal.</p>
	  <p>El EcoClear viene con tapas de acero para cerrar la parte superior de la unidad. Esto evita que caiga escombro o follaje en el sistema. Las tapas se abren para dar un fácil acceso a la cámara de lamelas. El acceso a la cámara se realizara periódicamente para lavar los bloques de lamelas maximizando el rendimiento de las mismas.</p>
	  <p>El EcoClear funciona de forma eficaz sin la necesidad de tratamiento químico. Esta unidad puede ser suministrada en conjunto con un Sistema de lavado de ruedas Wheelwash o instalado posteriormente.</p>
	  <p>Wheelwash también suministra la gama complete de sistemas de tratamiento de agua Cleartec lo que permite la descarga segura de los efluentes a los desagües públicos.</p>
