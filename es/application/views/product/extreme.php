      <p><b>El Rhino Ecowash Extreme es un sistema de lavado automático por aspersión de agua a alta presión diseñado para prevenir la extensión de lodos y residuos a las carreteras. Al igual que muchos de los sistemas de la gama Rhino Ecowash, funciona con bombas alimentadas a través de variadores de velocidad lo que proporciona ahorros de hasta un 50% en costes operacionales, un rendimiento sin igual, una elevada fiabilidad y una alta flexibilidad de presión de agua. Su modelo de operación en circuito cerrado permite la máxima reutilización del agua.</b></p>
	  <p>El Rhino Ecowash Extreme se compone de una robusta plataforma de lavado de 12m, cuatro rampas opcionales de 5m y un depósito para el reciclaje de agua con una capacidad de 56.000 litros. Todo fabricado en acero de alta resistencia. Las bombas son de alta calidad y el sistema de control está alojado en una sala de bobas dotada de cerradura.</p>
	  <p>La nueva y mejorada plataforma de lavado posee 8 barras de aspersión colocadas estratégicamente para obtener una cortina de agua de hasta 5 bar capaz de llegar a toda la superficie de cada rueda. El posicionamiento escalonado de las boquillas asegura un lavado eficaz de la totalidad de la rueda. El posicionamiento de cada barra y boquilla está diseñada para minimizar el exceso de pulverización y las altas pantallas laterales vienen de serie para asegurar que el agua de lavado sea devuelta nuevamente a la plataforma de lavado. El ciclo de lavado es gobernado de forma automática mediante sensores de entrada y salida, esto asegura que el agua este solamente rociando cuando hay un vehículo presente dentro de la plataforma de lavado.</p>
	  <p>El sumidero lateral, rejillas desmontables y su sistema de lavado automático convierten al Ecowash Extreme en un sistema más fácil de limpiar y mantener. Barreras de protección desmontables en tramos para facilitar la sustitución de las mismas en el improbable caso de que se dañen.</p>
	  <p>El depósito de sedimentación se encuentra dividido en tres partes divididas por placas perforadas y deflectores para ayudar a la separación de los residuos y mejorar la calidad del agua. La eliminación de los lodos sedimentados se realiza con facilidad mediante la apertura de una válvula situada en la parte inferior de cada uno de los depósitos. Un punto de aporte de agua limpia ubicado en el último compartimiento asegura que siempre hay suficiente agua para responder a la demanda. Equipada con una válvula de flotador, el depósito de agua se rellena automáticamente desde un suministro principal de agua, cuando sea necesario.</p>
	  <p>El sistema viene de estándar con una manguera de lavado para un lavado adicional y móvil.</p>
