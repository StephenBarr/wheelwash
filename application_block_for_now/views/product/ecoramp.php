      <p><b>The Rhino Ecoramp is a simple, drive-over dry ramp system designed to prevent the spread of site dirt and debris on to public highways. It is our most environmentally friendly wheel cleaning system because it requires no power or water plus it’s virtually maintenance-free. It works on the vibration effect created by the vehicle tyres driving over inverted steel angle.</b></p>
      <p>The system comprises robust mobile road in 4m long horizontal sections and low profile 3m inclined entry and exit ramps, all made from heavy duty steel. Each section is fitted with traffic barriers to provide a visual guide to drivers. Twin interlocks at both ends ensure fast installation and improved security of attachment. Spacer bars positioned at intervals between the two ramps provide additional rigidity.</p>
      <p>The 18m Rhino Ecoramp is suitable for vehicles up to 8 wheelers, the 22m version for articulated vehicles. Due to its modular format, any length of Ecoramp can be supplied to suit your space, usage and budget requirements.</p>
      <p>Vehicles mount the entry ramps and drive slowly over steel angle for 5.5 and 6.5 wheel revolutions respectively. This motion flexes open the tyre treads enabling dirt and debris to fall to the ground. The ramps’ component parts are easily lifted with site plant to enable access for removing accumulated dirt.</p>
      <p>The system is perfect for remote locations or for sites where there are no services available. It is delivered on a Hiab truck and is easy to install and relocate. Ecoramp systems are available for sale and hire.</p>
