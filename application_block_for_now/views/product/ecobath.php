      <p><b>The Rhino Ecobath is a simple, drive through bath system designed to prevent the spread of dirt and debris on to public highways from site vehicles and mobile plant. It is an environmentally friendly wheel cleaner that only requires 13,750 litres of water to become fully operational.</b></p>
      <p>The system comprises a 10m long central bath section containing removable, internal grids of steel angle that are partly submerged in water. Additional 3m long access ramps are supplied when the system is installed above ground. Sturdy horizontal road can also be supplied in 4m sections as an option to provide additional cleaning by means of a dry vibration effect. These can be located before and after the bath section and make it suitable for use by articulated vehicles.</p>
      <p>The standard 10m bath provides 3 wheel revolutions through the water. The steel angle flexes open the tyre treads as the vehicle passes over them, allowing dirt to fall out. The water washes the surface of the treads, ensuring an effective clean. The internal grids are easily removed to access the base of the bath for easy, periodic maintenance. Due to its modular format, any length of Ecobath can be supplied to suit your space, usage and budget requirements. Wheelwash can also provide bespoke versions on request.</p>
      <p>The Ecobath system is perfect for remote locations or for sites where there are no services available. It is delivered on a Hiab truck and is easy to install and relocate. Ecobath systems are available for sale and hire.</p>
